package al.strategies

import ml.Pattern
import ml.models.Model

trait MarginMeasure {
   protected def margin0(a: Array[Double]) = {
      val rank = a.sorted.reverse
      rank(0) - rank(1)
   }

   protected def margin(current_model: Model)(pa: Pattern) = margin0(current_model.distribution(pa))
}
